<script lang="ts" setup>
import './style.scss'
import { useOnDrop } from '../../../lib/composables/useOnDrop'

defineProps({
  id: {
    type: Number,
    default: null,
  },
})
const { isTarget, onDrop } = useOnDrop()
</script>

<template>
  <div
    class="drop-zone"
    :class="isTarget ? 'drop-zone_target' : false"
    @drop.prevent="onDrop($event, id)"
    @dragenter.prevent="isTarget = true"
    @dragover.prevent
    @dragleave="isTarget = false"
  >
    <slot></slot>
  </div>
</template>
